<p class="help"><i class="material-icons">info_outline</i>Here is a handy list of the latest updates we've done to Happy Web.</p>

<h3>28 May 2020</h3>
<p>There is a new option to export the whole website. It will create a zip file that you can download, and that contains all pages for your website. This way even if you prefer not to use HappyWeb, you can still have all your pages!</p>
<br />

<h3>27 May 2018</h3>
<p>If you have an eye for design and like to play with the visual aspect of your site, you can now change the colours and fonts of your website. The site comes with a pre-defined set of colours and fonts, but you can now override them if you so wish. More freedom = more web happiness!</p>
<br />

<h3>1 October 2017</h3>
<p>You can now use HappyWeb from your mobile phone! If you're on the go and need to quickly edit one of your pages, hop on to your admin site and edit the relevant page - everything will be accessible and will display properly.</p>
<p>Note that on a mobile phone the columns on a page wrap around and are displayed one under the other (rather then next to each other, because mobile phones have a narrow screen). So when you edit a page and change the amount of columns, don't be surprised if you don't see the columns one next to the other. On a regular screen they will display normally.</p>
<br />

<h3>12 September 2017</h3>
<p>We're on a roll! There is now a new option when creating audio file content. You can choose between uploading your own MP3 file, or you can simply enter the address of a SoundCloud page. Hurray! (If you have no idea what that means, not to worry, you probably don't need it. If you do need it it's really handy, though.)</p>
<br />

<h3>7 September 2017</h3>
<p>When creating or adding a new page, there is a new option called "hide this page". If you tick this option, the page will not show in the navigation. This can be used while you are working on a page and if you prefer to hide it until it's ready.</p> 
<p>There is also an option to hide a row on a page. You can click on the "cog" icon on a row, and you will see a new "hide this row" option. This will hide the row on the page. This can also be used if you are working on some content but prefer not to show it until it is finished.</p>